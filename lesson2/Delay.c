#include "Delay.h"

//========================================================================
// 函数：void delay(uint ms)
// 描述：延时函数，精确到 ms（准确度要求不高的情况下）
// 参数：ms
// 返回：
// 版本：
//========================================================================
void delay(uint ms)
{
	uint i, j;
	for(i = ms; i > 0; i--)
	{
		for(j = 115; j > 0; j--);
	}
}